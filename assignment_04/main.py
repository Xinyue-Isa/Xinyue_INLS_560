cars = ['audi', 'bmw', 'subaru', 'toyota']

for car in cars:
    if car == 'bmw': 
        print(car.upper())
    else: 
        print(car.title())


animals = ['Dog', 'Cat', 'Pig', 'Bird']

for animal in animals:
    if animal == 'Cat':
        print(animal.capitalize())
    else:
        print(animal.lower())
